public class supplier {
    private int s_suppkey;
    private String s_name;
    private String s_address;
    private String s_city;
    private String s_nation;
    private String s_region;
    private String s_phone;

    public supplier() {
    }

    public int getS_suppkey() {
        return s_suppkey;
    }

    public void setS_suppkey(int s_suppkey) {
        this.s_suppkey = s_suppkey;
    }

    public String getS_name() {
        return s_name;
    }

    public void setS_name(String s_name) {
        this.s_name = s_name;
    }

    public String getS_address() {
        return s_address;
    }

    public void setS_address(String s_address) {
        this.s_address = s_address;
    }

    public String getS_city() {
        return s_city;
    }

    public void setS_city(String s_city) {
        this.s_city = s_city;
    }

    public String getS_nation() {
        return s_nation;
    }

    public void setS_nation(String s_nation) {
        this.s_nation = s_nation;
    }

    public String getS_region() {
        return s_region;
    }

    public void setS_region(String s_region) {
        this.s_region = s_region;
    }

    public String getS_phone() {
        return s_phone;
    }

    public void setS_phone(String s_phone) {
        this.s_phone = s_phone;
    }
}